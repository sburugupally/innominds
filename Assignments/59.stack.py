#program to do all stack operations on lists
lst=["john","jack","games","sita","geetha"]
def push(lst):
    print(lst.append("ram"))
    print(lst)
def pop(lst):
    print(lst.pop())
    print(lst)
def peek(lst):
    print(lst[0])
    print(lst)
def quit(lst):
    print("quit")

push(lst)
pop(lst)
peek(lst)
quit(lst)

